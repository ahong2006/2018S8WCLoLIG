// Anime and Comic Derivative Products


public class AnimeFigure {

  private String series;        // Name of the anime/manga LIKE: Doraemon, Jujustsukaisen
  private String character;     // Name of the character , like: Satoru, Goku, Sakurajima Mai
  private String manufacturer;  // Company that produced the figure Like: Bandai, Good Smile , taito 
  private double scale;         // Scale of the figure compared to real life like: 1/8, 1/7, 1/1
  private String paintwork;     // Description of the paint application 
  private String material;      // Material the figure is made from ,like: PVC, ABS
  private String releaseDate;   // Date the figure was released
  private String accessories;   // Any additional items included with the figure , like :extra faces, weapons
}


